// Operator Modulus
// Operator Modulus adalah sebuah operasi yang menghasilkan sisa pembagian dari suatu bilangan (terhadap bilangan pembaginya). Di javascript, modulus dilambangkan dengan simbol persen (%)

// DIPERGUNAKAN MENCARI NILAI GANJIL DAN GENAP

// MENCARI NILAI HASIL BAGI DENGAN POWERFULL MODULUS 
