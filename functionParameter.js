
// string
function panggilParameters($string) {
    console.log($string);
}

panggilParameters('string')

// NUMBER
function panggilParameters2($number) {
    console.log($number);
}
panggilParameters2(123123)


// / NUMBER dan string
function panggilParameters3($number, $string) {
    console.log($number, $string);
}
panggilParameters3(123123, 'ahmad string');


// / NUMBER dan string dan array
function panggilParameters4($number, $string, $array) {
    console.log($number, $array);
    console.log($string);
}
panggilParameters4(123123, 'ahmad string', ['ahmad', 'badrul', 'ulum']);


