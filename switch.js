function switchCase() {
    var nilai = 6;
    switch (nilai) {
        case 1:
            console.log('satu');
            break;
        case 2:
            console.log('dua');
            break;
        case 3:
            console.log('tiga');
            break;
        default:
            console.log('empat');
            break;
    }
}


switchCase();